package com.backendhub.firstproject;

public class UnaryOpertor {
    public static void main(String[] args) {
        int i = 1;
        int j = ++i;
        System.out.println("value of i: "+i);
        System.out.println("value of j: "+j);
        i=3;
        int k = --i;

        System.out.println("value of i: "+i);
        System.out.println("value of k: "+k);
    }
}
